Time::Time(int duration)
{
	#ifdef DEBUG
		cout << "--- Appel du constructeur d'initialisation 2 ---" << endl;
	#endif

	if (duration / 60 == 0)
	{
		setMinute(duration);
		return;
	}

	setHour(duration/60);
	setMinute(duration%60);
}

//////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////

void Time::setHour(int h)
{
	if(h < 0 || h > 24) return;
	hour = h;
}

//////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////

void Time::setMinute(int m)
{
	if(m < 0 || m > 60) return;
	minute = m;
}

